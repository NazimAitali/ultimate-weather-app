export const darkChoise = (darkMode) => {
  return darkMode === "7%" ? "-44%" : "7%";
};
export const iconChoise = (iconDarkmode) => {
  return iconDarkmode === "4%" ? "42%" : "4%";
};

export const bgChanger = (bgColor) => {
  return bgColor === "#F8F8F8" ? "#18191A" : "#F8F8F8";
};
export const txtChanger = (txtColor) => {
  return txtColor === "black" ? "whitesmoke" : "black";
};
export const boxChanger = (bgBox) => {
  return bgBox === "white" ? "#242526" : "white";
};


